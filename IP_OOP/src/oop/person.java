package oop;

public class person {
	
	String fname;
	String bDay;
	int age;
	String nationality;
	String gender;

	//parameters
	person(String fname, String bDay, int age, String nationality, String gender){
		
		this.fname = fname;
		this.bDay = bDay;
		this.age = age;
		this.nationality = nationality;
		this.gender = gender;
		
	}
	
}
